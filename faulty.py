oper=input("Enter the operator\n")

n1=int(input("Enter 1st number\n"))
n2=int(input("Enter 2nd number\n"))



if oper=="+":
    if n1==56 and n2==9:
        print("555")
    else:
        print(n1+n2)

elif oper=="-":
    print(n1-n2)

elif oper=="*":
    if n1==45 and n2==3:
        print("555")
    else:
        print(n1*n2)
else:
    if n1==56 and n2==6:
        print("4")
    else:
        print(n1/n2)